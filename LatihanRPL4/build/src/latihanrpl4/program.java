    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int i = jTable1.getSelectedRow();
        if (i == -1) {
            return;
        }
        String Id = (String) model.getValueAt(i, 0);
        try {
            int hapus = JOptionPane.showConfirmDialog(this, "Apakah Anda Ingin Menghapus Data ? ", "Perhatian", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (hapus == JOptionPane.YES_OPTION)
            {
                JOptionPane.showMessageDialog(this, "Data Berhasil Di Hapus");
                Statement sql_hapus = (Statement) koneksi.getConnection().createStatement();
                sql_hapus.executeUpdate("delete from tbprogram where IdProgram = '" + Id + "'");
                sql_hapus.close();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Data Batal Di Hapus");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        loaddataprogram();
    }//GEN-LAST:event_jButton3ActionPerformed

