    void loaddatasekolah() {
        model.getDataVector().removeAllElements();
        try {
            Statement statement = (Statement) koneksi.getConnection().createStatement();
            String sql = "select * from namakota2";
            ResultSet r = statement.executeQuery(sql);
            while (r.next()) {
                Object[] o = new Object[4];
                o[0] = r.getString("IdSekolah");
                o[1] = r.getString("IdKota");
                o[2] = r.getString("NamaSekolah");
                o[3] = r.getString("NamaKota");
                model.addRow(o);
            }
            r.close();
            statement.close();
        } catch (Exception e) {
            System.out.println(e.getMessage());

        }
    }
