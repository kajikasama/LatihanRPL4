    void loaddatasekolah() {
        model.getDataVector().removeAllElements();
        try {
            Statement statement = (Statement) koneksi.getConnection().createStatement();
            String sql = "select * from ortu2";
            ResultSet r = statement.executeQuery(sql);
            while (r.next()) {
                Object[] o = new Object[8];
                o[0] = r.getString("IdOrtu");
//                o[7] = r.getString("IdKota");
                o[2] = r.getString("NamaOrtu");
                o[3] = r.getString("Pekerjaan");
                o[4] = r.getString("AlamatOrtu");
                o[5] = r.getString("TelpOrtu");
                o[6] = r.getString("HpOrtu");
                o[1] = r.getString("NamaKota");
                model.addRow(o);
            }
            r.close();
            statement.close();
        } catch (Exception e) {
            System.out.println(e.getMessage());

        }
    }
