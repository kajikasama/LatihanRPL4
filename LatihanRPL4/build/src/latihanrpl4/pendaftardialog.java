    void loaddataortu() {
        jComboBox4.removeAllItems();
        try {
            Statement statement = (Statement) koneksi.getConnection().createStatement();
            String sql = "select * from tbortu";
            ResultSet r = statement.executeQuery(sql);
            while (r.next())
            {
                jComboBox4.addItem(new dataortu(r.getString("IdOrtu"), r.getString("NamaOrtu")));
            }
            r.close();
            statement.close();
        } 
        catch (Exception e) {
            System.out.printf(e.getMessage());
        }
    }
