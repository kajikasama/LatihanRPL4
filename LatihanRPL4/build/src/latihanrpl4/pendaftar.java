    void loaddatasekolah() {
        model.getDataVector().removeAllElements();
        try {
            com.mysql.jdbc.Statement statement = (com.mysql.jdbc.Statement) koneksi.getConnection().createStatement();
            String sql = "SELECT pendaftar.* , tbsekolah.* , kota.* , tbprogram.* , tbortu.*  FROM pendaftar INNER JOIN tbsekolah ON pendaftar.IdSekolah=tbsekolah.IdSekolah INNER JOIN kota ON pendaftar.IdKota=kota.IdKota INNER JOIN tbprogram ON pendaftar.IdProgram = tbprogram.IdProgram INNER JOIN tbortu ON pendaftar.IdOrtu = tbortu.IdOrtu";
            ResultSet r = statement.executeQuery(sql);
            while (r.next()) {
                Object[] o = new Object[16];
                o[0] = r.getString("IdPendaftar");
                o[1] = r.getString("NamaaProgram");
                o[2] = r.getString("NamaKota");
                o[3] = r.getString("NamaOrtu");
                o[4] = r.getString("NamaSekolah");
                o[5] = r.getString("NoPeserta");
                o[6] = r.getString("TahunLulus");
                o[7] = r.getString("NamaPendaftar");
                o[8] = r.getString("Usia");
                o[9] = r.getString("TempatLahir");
                o[10] = r.getString("TglLahir");
                o[11] = r.getString("Jenskel");
                o[12] = r.getString("AlamatPendaftar");
                o[13] = r.getString("KotaTinggal");
                o[14] = r.getString("AsalSekolah");
                o[15] = r.getString("TelpPendaftar");
                model.addRow(o);
            }
            r.close();
            statement.close();
        } catch (Exception e) {
            System.out.println(e.getMessage());

        }
        
    }

